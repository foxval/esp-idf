typedef struct {
    uint32_t address;
    uint8_t ciphertext[128];
} aes_xts_test;

static void run_test_cases(const aes_xts_test *tests, size_t num_tests)
{
    const esp_partition_t *test_part = get_test_data_partition();

    /* Writing multiple lengths because this tests all the various
       block/offset combinations inside each 1024-bit block */
    const int lengths[] =   { 16, 32, 48, 64, 96, 128 };

    for (int i = 0; i < num_tests; i++) {
        size_t address = tests[i].address;
        const uint8_t *ciphertext = tests[i].ciphertext;
        // All the generated ciphertext addresses have to be inside the test partition
        TEST_ASSERT(address >= test_part->address);

        for (int l = 0; l < sizeof(lengths)/sizeof(int); l++) {
            size_t length = lengths[l];
            uint8_t readback[128] = { 0 };

            TEST_ASSERT(address + length <= test_part->address + test_part->size);

            printf("Writing %d encrypted bytes to 0x%x:\n", length, address);

            TEST_ASSERT_EQUAL(ESP_OK, spi_flash_erase_sector(address / SPI_FLASH_SEC_SIZE));
            TEST_ASSERT_EQUAL(ESP_OK, spi_flash_write_encrypted(address, plaintext, length));

            TEST_ASSERT_EQUAL(ESP_OK, spi_flash_read(address, readback, length));
            ESP_LOG_BUFFER_HEX("aes-xts readback", readback, length);
            ESP_LOG_BUFFER_HEX("expected", ciphertext, length);

            TEST_ASSERT_EQUAL_HEX8_ARRAY(ciphertext, readback, length);
        }
    }
}

